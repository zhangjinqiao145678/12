    公共工具_更新状态("托盘识别点2: 开始识别...", 状态回调)

    # [步骤1] 获取图像
    图像结果 = 公共工具_获取图像(图像接收接口)
    if 图像结果 is None:
        公共工具_更新状态("托盘识别点2: 未获取到图像", 状态回调)
        return

    图像数组, 宽度, 高度 = 图像结果

    # [步骤2] 调用托盘识别器进行识别
    公共工具_更新共享窗口(图像数组, 宽度, 高度, 窗口更新回调, "托盘识别点2 - 识别中")
    公共工具_更新状态("托盘识别点2: 正在识别托盘...", 状态回调)

    # 创建托盘识别器（托盘识别点2使用参数D）
    托盘识别器 = 托盘缺口识别器()

    # 执行托盘识别
    识别结果 = 托盘识别器.识别(图像数组)

    # [步骤3] 在图像上标记识别结果（只显示标记点，不显示文字）
    标记图像 = 托盘识别器.在图像上标记(图像数组, 识别结果, 显示文字=False)

    # 更新共享窗口显示标记后的图像
    公共工具_更新共享窗口(标记图像, 宽度, 高度, 窗口更新回调, "托盘识别点2 - 识别完成")

    # 打印识别结果
    缺口总数 = len(识别结果)
    if 缺口总数 > 0:
        旋转角度 = 识别结果[0].get('旋转角度', 0)
        print(f"[托盘识别点2] 识别到 {缺口总数} 个托盘缺口，托盘旋转角度: {旋转角度}°")
        for 结果 in 识别结果:
            x, y = 结果['中心点']
            形状类型 = 结果['形状类型']
            网格位置 = 结果['网格位置']
            print(f"  - {形状类型}: 位置({x}, {y}), 网格位置{网格位置}")
        公共工具_更新状态(f"托盘识别点2: 识别完成，{缺口总数}个缺口，旋转{旋转角度}°", 状态回调)
    else:
        公共工具_更新状态("托盘识别点2: 未识别到托盘缺口", 状态回调)
        print(f"[托盘识别点2] 未识别到任何托盘缺口")

    # [步骤4] 坐标转换（不影响画面显示，仅保存到文件）
    公共工具_更新状态("托盘识别点2: 正在进行坐标转换...", 状态回调)
    公共工具_执行坐标转换(识别结果, "托盘识别点2", is_tray=True)

    # [步骤5] 完成
    公共工具_更新共享窗口(标记图像, 宽度, 高度, 窗口更新回调, "托盘识别点2 - 已完成")
    公共工具_更新状态("托盘识别点2: 识别完成", 状态回调)
    print("[托盘识别点2] 识别流程结束")
