def 识别函数_物料识别点1(arm, 图像接收接口, 窗口更新回调, 状态回调):
    """物料识别点1 的识别处理函数

    流程：获取图像 -> 调用物料识别器 -> 在图像上标记结果 -> 坐标转换 -> 保存结果

    Args:
        arm: 机械臂对象
        图像接收接口: 摄像头图像接收接口实例
        窗口更新回调: 函数(图像数组, 宽度, 高度, 标题文本) -> None，在主线程更新共享窗口
        状态回调: 函数(状态文本) -> None，更新GUI状态显示
    """
    公共工具_更新状态("物料识别点1: 开始识别...", 状态回调)

    # [步骤1] 获取图像
    图像结果 = 公共工具_获取图像(图像接收接口)
    if 图像结果 is None:
        公共工具_更新状态("物料识别点1: 未获取到图像", 状态回调)
        return

    图像数组, 宽度, 高度 = 图像结果

    # [步骤2] 调用物料识别器进行识别
    公共工具_更新共享窗口(图像数组, 宽度, 高度, 窗口更新回调, "物料识别点1 - 识别中")
    公共工具_更新状态("物料识别点1: 正在识别物料...", 状态回调)

    # 创建物料识别器（物料识别点1使用参数A）
    物料识别器 = 多物料轮廓识别器()

    # 执行物料识别
    识别结果 = 物料识别器.识别所有物料(图像数组)

    # [步骤3] 在图像上标记识别结果（显示中文物料名称）
    标记图像 = 物料识别器.在图像上标记所有物料(图像数组, 识别结果, 显示文字=True)

    # 更新共享窗口显示标记后的图像
    公共工具_更新共享窗口(标记图像, 宽度, 高度, 窗口更新回调, "物料识别点1 - 识别完成")

    # 打印识别结果
    物料总数 = 0
    for 物料类型, 结果列表 in 识别结果.items():
        for 结果 in 结果列表:
            物料总数 += 1
            x, y = 结果['中心点']
            角度 = 结果.get('角度', 0.0)
            print(f"[物料识别点1] 识别到 {物料类型}: 位置({x}, {y}), 角度{角度:.1f}°")

    if 物料总数 > 0:
        公共工具_更新状态(f"物料识别点1: 识别完成，共{物料总数}个物料", 状态回调)
    else:
        公共工具_更新状态("物料识别点1: 未识别到物料", 状态回调)
        print(f"[物料识别点1] 未识别到任何物料")

    # [步骤4] 坐标转换（不影响画面显示，仅保存到文件）
    公共工具_更新状态("物料识别点1: 正在进行坐标转换...", 状态回调)
    公共工具_执行坐标转换(识别结果, "物料识别点1", is_tray=False)

    # [步骤5] 完成
    公共工具_更新共享窗口(标记图像, 宽度, 高度, 窗口更新回调, "物料识别点1 - 已完成")
    公共工具_更新状态("物料识别点1: 识别完成", 状态回调)
    print("[物料识别点1] 识别流程结束")
# 识别函数_物料识别点1() 结束


