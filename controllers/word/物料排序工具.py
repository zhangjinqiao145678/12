class 物料排序工具:
    """物料排序工具类

    提供物料列表的排序功能，支持多种排序策略
    """

    @staticmethod
    def 按Z轴降序排序(物料数据):
        """按物料的z轴坐标降序排序

        Args:
            物料数据: 物料位置数据对象，包含世界坐标系和机器人坐标系数据

        Returns:
            物料位置数据: 排序后的物料位置数据对象
        """
        from 物料位置数据 import 物料位置数据
        
        # 获取所有物料名称
        物料名称列表 = 物料数据.获取所有物料名称()
        
        # 按机器人坐标系的z坐标降序排序
        sorted_names = sorted(
            物料名称列表,
            key=lambda name: 物料数据.获取机器人坐标(name).get('z', 0),
            reverse=True
        )
        
        # 创建新的物料位置数据对象
        排序后的物料数据 = 物料位置数据()
        
        # 按排序后的顺序重新添加物料
        for 物料名称 in sorted_names:
            # 复制世界坐标系数据
            世界坐标 = 物料数据.获取世界坐标(物料名称)
            if 世界坐标:
                排序后的物料数据.设置世界坐标(
                    物料名称,
                    世界坐标.get('x', 0),
                    世界坐标.get('y', 0),
                    世界坐标.get('z', 0)
                )
                # 复制旋转信息
                旋转数据 = 物料数据.获取世界旋转(物料名称)
                if 旋转数据:
                    排序后的物料数据.设置世界旋转(
                        物料名称,
                        旋转数据.get('rx', 0),
                        旋转数据.get('ry', 0),
                        旋转数据.get('rz', 0),
                        旋转数据.get('angle', 0)
                    )
            
            # 复制机器人坐标系数据
            机器人坐标 = 物料数据.获取机器人坐标(物料名称)
            if 机器人坐标:
                排序后的物料数据.设置机器人坐标(
                    物料名称,
                    机器人坐标.get('x', 0),
                    机器人坐标.get('y', 0),
                    机器人坐标.get('z', 0),
                    机器人坐标.get('rx', 0),
                    机器人坐标.get('ry', 0),
                    机器人坐标.get('rz', 0)
                )
        
        return 排序后的物料数据

