def 公共工具_获取图像(图像接收接口):
    """从摄像头接收接口获取当前一帧图像

    Args:
        图像接收接口: 摄像头图像接收接口实例

    Returns:
        tuple: (图像数组numpy, 宽度int, 高度int) 读取失败返回 None
    """
    if 图像接收接口 is None:
        return None
    结果 = 图像接收接口.读取图像(转换为BGR=True)
    return 结果
# 公共工具_获取图像() 结束


def 公共工具_更新共享窗口(图像数组, 宽度, 高度, 窗口更新回调, 标题文本):
    """通过窗口更新回调更新共享识别窗口的图像和标题

    Args:
        图像数组: numpy 数组 (H, W, 3)，BGR格式
        宽度: 图像宽度
        高度: 图像高度
        窗口更新回调: 一个函数，签名为 (图像数组, 宽度, 高度, 标题文本) -> None
                      该回调内部会通过 root.after() 在主线程执行实际更新
        标题文本: 要显示在窗口标题栏的文本
    """
    if callable(窗口更新回调):
        窗口更新回调(图像数组, 宽度, 高度, 标题文本)
# 公共工具_更新共享窗口() 结束


def 公共工具_更新状态(文本, 状态回调):
    """通过状态回调更新当前处理状态

    Args:
        文本: 要显示的状态文本
        状态回调: 一个函数，签名为 (文本) -> None
    """
    if callable(状态回调):
        状态回调(文本)
# 公共工具_更新状态() 结束


def 公共工具_保存视觉识别结果(识别结果, 文件名称, 识别点名称, is_tray=False):
    """保存视觉识别结果到通信中间文件

    Args:
        识别结果: 识别结果字典
        文件名称: 保存的文件名（不含路径）
        识别点名称: 当前识别点名称
        is_tray: 是否为托盘识别
    """
    try:
        # 确保目录存在
        if not os.path.exists(通信中间文件目录):
            os.makedirs(通信中间文件目录, exist_ok=True)
        
        文件路径 = os.path.join(通信中间文件目录, 文件名称)
        
        # 构建保存的数据结构
        保存数据 = {
            '识别点': 识别点名称,
            '识别类型': '托盘缺口' if is_tray else '物料',
            '时间戳': int(time.time()),
            '识别结果': 识别结果
        }
        
        with open(文件路径, 'w', encoding='utf-8') as f:
            json.dump(保存数据, f, indent=4, ensure_ascii=False)
        
        print(f"[识别模块] 视觉识别结果已保存到: {文件路径}")
    except Exception as e:
        print(f"[识别模块] 保存视觉识别结果失败: {e}")
# 公共工具_保存视觉识别结果() 结束


def 公共工具_执行坐标转换(识别结果, 识别点名称, is_tray=False):
    """执行坐标转换并保存结果（不影响画面显示）

    Args:
        识别结果: 识别结果字典
        识别点名称: 当前识别点名称
        is_tray: 是否为托盘识别

    Returns:
        bool: 是否转换成功
    """
    try:
        # 创建坐标转换器
        转换器 = 手眼标定坐标转换器()
        
        # 切换到当前识别点的参数
        转换器.切换识别点参数(识别点名称)
        
        # 构建坐标转换所需的输入格式
        if is_tray:
            # 托盘缺口识别结果转换
            缺口详情 = {}
            for idx, 结果 in enumerate(识别结果):
                缺口详情[f"缺口{idx+1}"] = {
                    '像素坐标_x': 结果['中心点'][0],
                    '像素坐标_y': 结果['中心点'][1],
                    '旋转角度': 结果.get('旋转角度', 0),
                    '缺口类型': 结果['形状类型'],
                    '网格位置': str(结果['网格位置'])
                }
            转换输入 = {'缺口详情': 缺口详情}
            
            # 执行转换
            转换结果 = 转换器.转换视觉识别结果(转换输入, 识别点名称, is_tray=True)
            
            # 保存转换结果
            输出文件 = os.path.join(通信中间文件目录, '托盘缺口_转换结果.json')
            转换器.保存转换结果(转换结果, 输出文件, 识别点名称, is_tray=True)
            
            # 保存原始像素坐标
            像素文件 = os.path.join(通信中间文件目录, '托盘缺口位置.json')
            公共工具_保存视觉识别结果(识别结果, '托盘缺口位置.json', 识别点名称, is_tray=True)
        else:
            # 物料识别结果转换（只输出，不保存到文件）
            物料详情 = {}
            物料编号 = 0
            for 物料类型, 结果列表 in 识别结果.items():
                for 结果 in 结果列表:
                    物料编号 += 1
                    物料名称 = f"{物料类型}" if 物料编号 == 1 else f"{物料类型}({物料编号-1})"
                    物料详情[物料名称] = {
                        '像素坐标_x': 结果['中心点'][0],
                        '像素坐标_y': 结果['中心点'][1],
                        '旋转角度': 结果.get('角度', 0),
                        '物料类型': 物料类型
                    }
            转换输入 = {'物料详情': 物料详情}
            
            # 执行转换
            转换结果 = 转换器.转换视觉识别结果(转换输入, 识别点名称, is_tray=False)
            
            # 输出转换结果到控制台（不保存到文件）
            print(f"[识别模块] {识别点名称} 物料坐标转换结果:")
            for 物料名称, 坐标信息 in 转换结果.items():
                if isinstance(坐标信息, dict) and '机器人坐标' in 坐标信息:
                    机器人坐标 = 坐标信息['机器人坐标']
                    print(f"  {物料名称}: 像素坐标({坐标信息.get('像素坐标_x', 'N/A')}, {坐标信息.get('像素坐标_y', 'N/A')}) -> 机器人坐标({机器人坐标.get('x', 'N/A')}, {机器人坐标.get('y', 'N/A')})")
        
        print(f"[识别模块] {识别点名称} 坐标转换完成")
        return True
    except Exception as e:
        print(f"[识别模块] {识别点名称} 坐标转换失败: {e}")
        return False
# 公共工具_执行坐标转换() 结束


