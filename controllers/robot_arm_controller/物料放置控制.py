import time

class 物料放置控制器:
    """物料放置控制器类
    
    负责根据物料类型和抓取次数确定放置位置
    支持多种物料类型，每种物料最多支持两个放置位置
    """
    
    def __init__(self):
        """初始化放置位置配置"""
        # 放置位置配置字典
        # 格式: 物料类型 -> [第一个放置位置, 第二个放置位置]
        # 位置格式: [x, y, z, rx, ry, rz]
        self.放置位置配置 = {
            'Cruciform': [
                [0.234, 0.155, 0.04, -3.1415926535, 0, 0],  # 第一个十字形放置位置
                [0.057, 0.160, 0.04, -3.1415926535, 0, 0],  # 第二个十字形放置位置
            ],
            'Cuboid': [
                [0.228, 0.213, 0.02, -3.1415926535, 0, 0],     # 第一个长方形放置位置
                [0.057, 0.216, 0.02, -3.1415926535, 0, 0],     # 第二个长方形放置位置
            ],
            'FivePointed': [
                [0.173, 0.16, 0.02, -3.1415926535, 0, 0],    # 第一个五角星放置位置
                [-0.007, 0.164, 0.02, -3.1415926535, 0, 0],  # 第二个五角星放置位置
            ],
            'Cylindrical': [
                [0.118, 0.272, 0.04, -3.1415926535, 0, 0],  # 第一个圆柱体放置位置
                [-0.062, 0.275, 0.04, -3.1415926535, 0, 0], # 第二个圆柱体放置位置
            ],
            'Cube': [
                [0.234, 0.267, 0.03, -3.1415926535, 0, 0],  # 第一个正方形放置位置
                [0.057, 0.272, 0.025, -3.1415926535, 0, 0], # 第二个正方形放置位置
            ],
            'Parallelogram': [
                [0.120, 0.217, 0.02, -3.1415926535, 0, 0],     # 第一个平行四边形放置位置
                [-0.060, 0.2215, 0.02, -3.1415926535, 0, 0],   # 第二个平行四边形放置位置
            ],
            'Triangle': [
                [0.163, 0.268, 0.02, -3.1415926535, 0, 0],  # 第一个三角形放置位置
                [-0.017, 0.273, 0.02, -3.1415926535, 0, 0], # 第二个三角形放置位置
            ],
            'Pentagonal': [
                [0.173, 0.219, 0.02, -3.1415926535, 0, 0],  # 第一个五边形放置位置
                [-0.007, 0.221, 0.02, -3.1415926535, 0, 0], # 第二个五边形放置位置
            ],
            'Quincunx': [
                [-0.065, 0.165, 0.02, -3.1415926535, 0, 0],  # 第一个梅花形放置位置
                [0.117, 0.160, 0.02, -3.1415926535, 0, 0], # 第二个梅花形放置位置
            ],
        }
        
        # 记录每种物料已放置的次数
        self.物料放置计数 = {key: 0 for key in self.放置位置配置.keys()}
    
    def 获取放置位置(self, 物料类型):
        """获取指定物料类型的放置位置
        
        Args:
            物料类型: 物料的类型名称（如 'FivePointed', 'Cuboid', 'Parallelogram'）
        
        Returns:
            list: 放置位置 [x, y, z, rx, ry, rz]，如果物料类型未知返回None
        """
        if 物料类型 not in self.放置位置配置:
            print(f"警告: 未知物料类型 '{物料类型}'，使用默认放置位置")
            return [0.2, 0.3, 0.3, -3.1415926535, 0, 0]
        
        # 获取当前应该使用的放置位置索引
        当前索引 = self.物料放置计数[物料类型]
        
        # 获取放置位置列表
        放置位置列表 = self.放置位置配置[物料类型]
        
        # 如果超过配置的位置数量，循环使用
        if 当前索引 >= len(放置位置列表):
            当前索引 = 当前索引 % len(放置位置列表)
        
        # 获取放置位置
        放置位置 = 放置位置列表[当前索引]
        
        # 更新放置计数
        self.物料放置计数[物料类型] += 1
        
        return 放置位置
    
    def 执行放置(self, arm, 物料类型, 延时时间=2.0):
        """执行物料放置操作

        Args:
            arm: 机械臂对象
            物料类型: 物料的类型名称
            延时时间: 到达位置后的等待时间（秒），用于确保放置完成

        Returns:
            bool: 放置是否成功
        """
        try:
            # 获取放置位置
            放置位置 = self.获取放置位置(物料类型)
            if 放置位置 is None:
                return False
            
            x, y, z, rx, ry, rz = 放置位置
            
            # 步骤1: 移动到放置位置上方0.07米处（安全高度）
            arm.move_to_pose(x, y, 0.07, rx, ry, rz, duration=2)
            
            # 等待到达安全高度
            time.sleep(延时时间)
            
            # 步骤2: 移动到最终放置位置（z=0.04米）
            arm.move_to_pose(x, y, z, rx, ry, rz, duration=1)
            
            # 等待到达放置位置
            time.sleep(延时时间)
            
            # 关闭吸盘释放物料
            arm.disable_end_effector()

            # 等待物料释放
            time.sleep(延时时间)
            
            # 移动回安全高度
            arm.move_to_pose(x, y, z + 0.1, rx, ry, rz, duration=1)
            
            print(f"物料 '{物料类型}' 已放置到位置: X={x}, Y={y}, Z={z}")
            return True
        
        except Exception as e:
            print(f"放置物料失败: {str(e)}")
            return False
    
    def 重置放置计数(self, 物料类型=None):
        """重置物料放置计数
        
        Args:
            物料类型: 可选，指定要重置的物料类型。如果为None，则重置所有物料类型的计数
        """
        if 物料类型 is None:
            for key in self.物料放置计数.keys():
                self.物料放置计数[key] = 0
        elif 物料类型 in self.物料放置计数:
            self.物料放置计数[物料类型] = 0
    
    def 添加物料配置(self, 物料类型, 放置位置列表):
        """添加新的物料类型配置
        
        Args:
            物料类型: 物料类型名称
            放置位置列表: 放置位置列表，每个位置格式为 [x, y, z, rx, ry, rz]
        """
        self.放置位置配置[物料类型] = 放置位置列表
        self.物料放置计数[物料类型] = 0
    
    def 更新物料配置(self, 物料类型, 放置位置列表):
        """更新已有物料类型的配置
        
        Args:
            物料类型: 物料类型名称
            放置位置列表: 新的放置位置列表
        """
        if 物料类型 in self.放置位置配置:
            self.放置位置配置[物料类型] = 放置位置列表
        else:
            self.add_material_config(物料类型, 放置位置列表)
