def 示例1_简单姿态变换():
    """示例1：使用简单姿态变换（仅调整rz角度）"""
    print("=" * 60)
    print("示例1：简单姿态变换")
    print("=" * 60)
    
    # 物料旋转（轴角表示）：绕Z轴旋转45度
    物料旋转轴角 = [0.0, 0.0, 1.0, np.pi / 4]
    
    print(f"物料旋转（轴角）：{物料旋转轴角}")
    
    # 使用简单姿态变换
    姿态 = 姿态变换工具.计算简单抓取姿态(物料旋转轴角)
    
    print(f"计算结果（机器人坐标系）：")
